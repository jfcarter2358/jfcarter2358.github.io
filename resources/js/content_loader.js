$(document).ready(
    function()
    {
        // get current page name
        var urlParts = $(location).attr("href").split("/");
        var pageName = urlParts.pop().split(".")[0];

        // define variables
        var contentTitle = document.getElementById("content-title");
        var contentPanel = document.getElementById("content-panel");

        var content = "";

        for (var b in pageContent[pageName]["blocks"])
        {
            // add in section title as header
            content += "<h4>" + pageContent[pageName]["blocks"][b]["title"] + "</h4>";
            // create section content list and populate it
            content += "<ul>";
            for (var c in pageContent[pageName]["blocks"][b]["content"])
            {
                content += "<li>" + pageContent[pageName]["blocks"][b]["content"][c] + "</li>";
            }
            content += "</ul>";
            // add spacing between sections
            content += "<br>";
        }

        // populate content and title elements with data
        contentTitle.innerHTML = "<h3>" + pageContent[pageName]["title"] + "</h3>";
        contentPanel.innerHTML = content;
    }
);